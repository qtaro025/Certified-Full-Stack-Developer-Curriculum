/**
 * The split() method divides a string into an array of substrings and
 * specifies where each split should happen based on a given separator.
 * If no separator is provided, the method returns an array containing
 * the original string as a single element.
 */
let str = "hello";
let charArray = str.split("");
console.log(charArray); // ["h", "e", "l", "l", "o"]
